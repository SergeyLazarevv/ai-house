"""Сборка промптов оркестратора и синтеза."""

from __future__ import annotations

from app.orchestration.scenarios import load_scenarios_text
from app.orchestration.state import GraphState


def summarize_state(state: GraphState, limit: int = 6000) -> str:
    def clip(value: str) -> str:
        text = (value or "").strip()
        if len(text) <= limit:
            return text or "(пусто)"
        return text[: limit - 20] + "\n…(обрезано)"

    return (
        f"## БД\n{clip(state.get('db_result', ''))}\n\n"
        f"## Логи\n{clip(state.get('logs_result', ''))}\n\n"
        f"## Код\n{clip(state.get('code_result', ''))}"
    )


def build_supervisor_system_prompt(allowed_roles: list[str]) -> str:
    allowed_str = ", ".join(allowed_roles) if allowed_roles else "general"
    scenarios = load_scenarios_text()
    base = (
        "Ты оркестратор мультиагентной поддержки. Ты один видишь полный вопрос пользователя и полные результаты "
        "прошлых шагов; специалисты — нет: им нельзя пересылать сырой вопрос целиком и полные дампы других систем.\n"
        f"Доступные специалисты (только из списка): {allowed_str}. Ещё вариант: finish — хватит данных, финальный ответ "
        "соберёт другой узел.\n"
        "На каждом шаге (кроме finish) ты обязан сформулировать:\n"
        "- task — одно чёткое техническое задание для выбранного специалиста. Без отвлечений про другие сервисы, "
        "без пересказа вопроса пользователя, только инструкция исполнителю.\n"
        "- context_hint — коротко (до ~1–2 предложений) только те факты из уже полученных результатов, без которых "
        "специалист не справится. Если ничего не нужно — пустая строка.\n"
        "Правила:\n"
        "- Не вызывай специалиста без нужды.\n"
        "- Общий smalltalk — general.\n"
        "- Вопросы про **логи**, **ошибки в логах**, **Graylog**, **топ частых ошибок**, **сколько ошибок**, **inhouse**/input — при доступном специалисте **logs**: передай ему чёткую задачу про период, имя окружения и ожидаемый тип ответа; выбор конкретного инструмента он сделает по MCP-контракту сам.\n"
        "- Если пользователь приводит точный текст ошибки/исключения и просит **причину**, **полный лог**, **stack trace**, **пример записи**, **full message** или **что указано в логе**, сначала всегда выбирай **logs**. Даже если ошибка похожа на баг приложения, сначала нужно получить факты из логов; в **code** переходи только если логов уже недостаточно.\n"
        "- Если данных достаточно, выбирай **finish**.\n"
        "- После шага **logs** / **db** / **code**: если соответствующий блок результатов (**## Логи** и т.д.) уже не пустой и по смыслу закрывает вопрос — сразу **finish**. Не звони в **general** «для красивого пересказа»: итоговый ответ для пользователя собирает узел **synthesize** из этих блоков; лишний **general** только дублирует LLM и путает метку «задействовано».\n"
        "- Если специалист вернул внутреннюю или инструментальную ошибку (например 'Агент логов:', 'лимит итераций', HTTP/401/403/404/5xx, 'Graylog не настроен', 'Ошибка MCP'), не отправляй тот же запрос этому же специалисту повторно. Сразу выбирай **finish**.\n"
        "- Code нужен только когда БД/логи уже не объясняют проблему, а в данных есть признаки бага приложения.\n"
    )
    if scenarios:
        base += "\nСценарии расследования:\n" + scenarios + "\n"
    base += (
        "\nОтветь только JSON без markdown:\n"
        '{"next":"db|logs|code|general|finish","task":"...","context_hint":"...","reason":"кратко"}\n'
        "При next=finish поля task и context_hint оставь пустыми строками.\n"
    )
    return base


def build_synthesize_system_prompt() -> str:
    return (
        "Ты ведущий инженер поддержки. Специалисты уже отработали под управлением оркестратора; "
        "ниже их результаты (часть блоков может быть пуста). Сведи всё в один связный ответ на языке "
        "пользователя: статус, причины, выводы. Не выдумывай факты, опирайся только на данные выше.\n"
        "Формат ответа должен быть единообразным для любых вопросов:\n"
        "1) Кратко: 1-2 предложения по сути.\n"
        "2) Факты: 2-5 буллетов только с подтверждёнными данными.\n"
        "3) Вывод: что это означает или что делать дальше.\n"
        "Если блок **Логи** содержит ошибку инструмента (HTTP 4xx/5xx, 'Сеть / запрос', 'не удалось'), "
        "не переписывай её в догадки: покажи её напрямую и кратко.\n"
        "Если блок **Логи** содержит внутреннюю ошибку агента ('Агент логов:', 'лимит итераций', 'Graylog не настроен', 'Ошибка MCP'), "
        "не представляй её как ошибку из продуктовых логов и не выдумывай причины в системе пользователя.\n"
        "Если в блоке **Логи** есть JSON с бакетами и count: при вопросе про топ/список/статистику "
        "ошибок перечисли **разные ключи** (тексты message/logger) с **count каждого** из этого JSON. "
        "Не заполняй «топ N» повторением одного общего total по уровню ERROR/3."
    )
